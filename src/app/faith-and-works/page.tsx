function AccordionItem({
  title,
  body,
  expanded,
  onToggle,
}: {
  title: string;
  body: string;
  expanded: boolean;
  onToggle: () => void;
}) {
  return (
    <div
      style={{
        background: CARD,
        border: `1px solid ${expanded ? PURPLE : BORDER}`,
        borderRadius: 10,
        marginBottom: 10,
        overflow: "hidden",
        transition: "border-color 0.2s",
      }}
    >
      <button type="button"
        onClick={onToggle}
        style={{
          width: "100%",
          background: "transparent",
          border: "none",
          padding: "16px 20px",
          display: "flex",
          justifyContent: "space-between",
          alignItems: "center",
          cursor: "pointer",
          textAlign: "left",
        }}
      >
        <span style={{ color: expanded ? GREEN : TEXT, fontWeight: 700, fontSize: 15 }}>
          {title}
        </span>
        <span
          style={{
            color: MUTED,
            fontSize: 18,
            flexShrink: 0,
            marginLeft: 12,
            transform: expanded ? "rotate(45deg)" : "rotate(0deg)",
            transition: "transform 0.2s",
            display: "inline-block",
          }}
        >
          +
        </span>
      </button>
      {expanded && (
        <div style={{ padding: "0 20px 18px", borderTop: `1px solid ${BORDER}` }}>
          <p style={{ color: TEXT, lineHeight: 1.8, fontSize: 14, margin: "14px 0 0" }}>{body}</p>
        </div>
      )}
    </div>
  );
}

